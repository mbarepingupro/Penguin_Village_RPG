def pick_autonomous_action(penguin, all_penguins):
    traits      = [penguin.get("trait_social"), penguin.get("trait_interest"), penguin.get("trait_quirk")]
    social_mode = penguin.get("social_mode") or "social"
    others      = [p for p in all_penguins if p["username"] != penguin["username"]]
    has_interests = bool(penguin.get("interests"))

    selected_topics = penguin.get("interests") or []

    scored = []
    for action in AUTONOMOUS_ACTIONS:
        requires_other    = action.get("requires_other", False)
        requires_interest = action.get("requires_interest", False)
        requires_topic    = action.get("requires_topic")
        if requires_other and not others:
            continue
        if requires_interest and not has_interests:
            continue
        # Stricter than requires_interest above: this template hardcodes one
        # specific topic's label/emoji into its text, so it only fires for a
        # player who selected that exact topic (not just "any interest").
        if requires_topic and requires_topic not in selected_topics:
            continue

        score = sum(action["weights"].get(t, 0) for t in traits if t) or 1

        if social_mode == "homebody":
            if requires_other:
                score = max(1, score // 4)   # drastically reduce social
            else:
                score = int(score * 1.5)     # boost solo
        elif social_mode == "social":
            if requires_other:
                score = int(score * 1.3)     # slightly boost social
        elif social_mode == "focused":
            if requires_other:
                score = int(score * 1.2)     # slightly boost social (target handled in pick_other)

        scored.append((action, score))

    if not scored:
        return random.choice(AUTONOMOUS_ACTIONS)

    total = sum(s for _, s in scored)
    roll  = random.uniform(0, total)
    cumulative = 0
    for action, score in scored:
        cumulative += score
        if roll <= cumulative:
            return action
    return scored[-1][0]


def pick_other_penguin(penguin, all_penguins):
    others = [p for p in all_penguins if p["username"] != penguin["username"]]
    if not others:
        return None
    social_mode   = penguin.get("social_mode") or "social"
    social_target = penguin.get("social_target")
    if social_mode == "focused" and social_target:
        target = next((p for p in others if p["username"] == social_target), None)
        if target and random.random() < 0.60:
            return target
    return random.choice(others)


def highlight_name(name):
    """Wraps a player/penguin display name for visual emphasis wherever an
    event log message or autonomous action gets rendered (event log tab,
    welcome-back popup, news ticker — all share the same event_log.message
    text). Purple #A86EFF, consistent with the existing palette."""
    return f'<span class="pname-hl">{name}</span>'


def highlight_interest(label):
    """Wraps an interest/topic label for visual emphasis, mirroring
    highlight_name() above but in the palette orange (#FF8C00). Applied at
    template-fill time so newly generated events carry the highlight baked
    in; pre-existing plain-text rows in event_log are left as-is."""
    return f'<span class="topic-hl">{label}</span>'


def generate_action_text(action, penguin, other_penguin=None):
    pname = penguin.get("penguin_name") or penguin["username"]
    text = action["template"].replace("{penguin}", highlight_name(pname))
    if action.get("requires_other") and "{other}" in text and other_penguin:
        oname = other_penguin.get("penguin_name") or other_penguin["username"]
        text = text.replace("{other}", highlight_name(oname))
    if action.get("requires_interest") and "{interest}" in text:
        interests = penguin.get("interests") or []
        if interests:
            chosen = random.choice(interests)
            topic  = INTEREST_TOPICS.get(chosen)
            label  = f"{topic['emoji']} {topic['label']}" if topic else chosen
        else:
            label = "something"
        text = text.replace("{interest}", highlight_interest(label))
    return text


def pick_group_event(topic_to_players):
    """topic_to_players: {topic_key: [(username, display_name), ...]} for every
    topic currently selected by at least one player.

    Picks one topic that has >=2 interested players, then one matching
    GROUP_EVENT_TEMPLATES entry for it. Returns (topic_key, template_entry,
    [(username, display_name), ...]) or None if no topic currently qualifies --
    callers should skip silently in that case (no error, no fallback)."""
    eligible = {k: v for k, v in topic_to_players.items() if len(v) >= 2}
    if not eligible:
        return None
    topic_key = random.choice(list(eligible.keys()))
    matching = [e for e in GROUP_EVENT_TEMPLATES if e.get("requires_topic") == topic_key]
    if not matching:
        return None
    entry = random.choice(matching)
    return topic_key, entry, eligible[topic_key]


def format_group_event_text(entry, participants, topic_key=None):
    """Group event templates describe a village-wide happening with no
    {penguin}/{other} slots (see GROUP_EVENT_TEMPLATES) -- the participant
    list is appended and highlighted separately so the event still credits
    everyone who took part. participants: [(username, display_name), ...].

    Unlike the autonomous-action templates, the topic label here is baked
    directly into the template prose (often repeated) rather than exposed
    as a {interest} slot, so it's highlighted by substring replacement
    against the topic's own label/emoji instead of a placeholder swap."""
    template = entry["template"]
    topic = INTEREST_TOPICS.get(topic_key) if topic_key else None
    if topic:
        label   = topic["label"]
        # Matches "Cooking 🍳" as one unit where the emoji immediately follows
        # (the first mention in every template), and bare "Cooking" everywhere
        # else -- single pass so neither occurrence gets wrapped twice.
        pattern = re.escape(label) + r"(?: " + re.escape(topic["emoji"]) + r")?"
        template = re.sub(pattern, lambda m: highlight_interest(m.group(0)), template)
    highlighted = [highlight_name(display_name) for (_, display_name) in participants]
    if len(highlighted) == 1:
        names_str = highlighted[0]
    else:
        names_str = ", ".join(highlighted[:-1]) + " and " + highlighted[-1]
    return f"{template} — {names_str}"
